class relationaloperator{
	public static void main(String[] args){
		int s=8;
		int p=8;

		System.out.println(s==p);//true
		System.out.println(s<p);//false
		System.out.println(s>p);//false
		System.out.println(s>=p);//true
		System.out.println(s<=p);//true
		System.out.println(s!=p);//flase
	}
}
